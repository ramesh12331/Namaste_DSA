// Initialize the variable i with value 0
let i = 0;

// Loop will run as long as the condition (i < 5) is true
while (i < 5) {

    // Print "Hello World" to the console
    console.log("Hello World");

    // Increment i by 1 to avoid an infinite loop
    i++;
}

